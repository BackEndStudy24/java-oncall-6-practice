package oncall.model;

public class StartDay {

    private int month;
    private String strDay;


    public void setMonth(int month) {
        this.month = month;
    }

    public void setStrDay(String strDay) {
        this.strDay = strDay;
    }

    public int getMonth() {
        return month;
    }

    public String getStrDay() {
        return strDay;
    }
}
